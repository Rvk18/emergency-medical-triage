/**
 * Get current language
 * @returns {string} Language code
 */
export function getCurrentLanguage() {
  return localStorage.getItem(LANG_KEY) || 'en';
}

/**
 * Set language
 * @param {string} code - Language code
 */
export function setLanguage(code) {
  if (!LANGUAGES[code]) {
    console.error('[i18n] Invalid language code:', code);
    return;
  }
  
  localStorage.setItem(LANG_KEY, code);
  console.log('[i18n] Language set:', code);
  
  // Dispatch event for components to update
  const event = new CustomEvent('languagechange', { detail: { code } });
  console.log('[i18n] Dispatching languagechange event:', event);
  window.dispatchEvent(event);
  console.log('[i18n] Event dispatched successfully');
}

/**
 * Translate key
 * @param {string} key - Translation key
 * @returns {string} Translated text
 */
export function t(key) {
  const lang = getCurrentLanguage();
  const langTranslations = translations[lang] || translations.en;
  const translation = langTranslations[key];
  
  // Fallback to English if translation not found
  if (!translation && lang !== 'en') {
    return translations.en[key] || key;
  }
  
  return translation || key;
}

